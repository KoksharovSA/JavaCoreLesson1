package ru.gb.regular;

/**
 * Калькулятор
 */
public class OtherClass {
    /**
     * @param a первая переменная
     * @param b вторая переменная
     * @return результат сложения переменных
     */
    public static int add(int a, int b){
        return a + b;
    }

    /**
     * @param a первая переменная
     * @param b вторая переменная
     * @return результат деления переменных
     */
    public static int div(int a, int b){
        return a / b;
    }

    /**
     * @param a первая переменная
     * @param b вторая переменная
     * @return результат умножения переменных
     */
    public static int mul(int a, int b){
        return a * b;
    }

    /**
     * @param a первая переменная
     * @param b вторая переменная
     * @return результат вычитания переменных
     */
    public static int sub(int a, int b){
        return a - b;
    }
}
